        <h3>Vision</h3> 
        <p>No one shall die in Dhanbad for the want of Blood. This has been our motto since the inception of Bloodline. To achieve this, our current objective is to register at least 1,000 new donors in a year by means of donor motivation, promotion of the concept of Voluntary Blood Donation and by repeating Regular Voluntary Blood Donation campaigns.

        </p>
      </div>  <br><hr style="color: black">
      <div id="section3">
        <h3>Working</h3>
        <p> At the start of each academic session, Bloodline conducts a presentation and a survey among all the students, faculty members, security guards and other individuals of ISM and collects the contacts, blood group and other relevant details of all those who are interested to donate blood. When any blood request comes, the database is referred to and an individual with required blood group is contacted for blood donation. One of the volunteers accompanies the donor and ensures a safe donation process.     </p>
        <h4>BLOOD CAMPS</h4>
        <p>Blood camps are organized once every three months. A number of students from ISM Dhanbad voluntarily donate blood in these camps.</p>
        <h4>SCHOOL VISITS</h4>
        <p>Team Bloodline regularly visits the schools of Dhanbad and its nearby villages giving important information about blood donations. Although this isn’t a very attractive prospect for the short term, we expect it to reap dividends in the longer run when these kids grow up to be civilized citizens of the country. Inculcating these qualities in the children at an early stage should prove important in realizing our long term vision.

        </p>
        <h4>NUKKAD NATIKA</h4>
        <p>For spreading awareness among the general public and for motivating them for blood donation, Nukkad Natikas (street plays) are organized. The Nukkad Natak team comprising of boys and girls perform Nukkad Nataks during the camps. Special Nukkad Nataks are performed in the slums, at Dhanbad railway station and other important places.</p>
