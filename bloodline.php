          <h2>You Can Be A Donor !!!!!</h2> 
          <h3>DOs and DON'Ts of blood donation:</h3>
          <p>In order to ensure safe and health environment to both donors as well as recipients, we greatly recommend to follow the below rules:

          </p>
          <h3>DO donate blood, only if you satisfy all of the following conditions</h3>
          <p><ul>
            <li><p>You are between age group of 18-60 years.</li>
            <li><p>Your weight is 45 kgs or more.</li>
            <li><p>Your hemoglobin is 12.5 gm/ml minimum.</li>
            <li><p>Your last blood donation was 3 or more months earlier.</li>
            <li><p>You are healthy and have not suffered from malaria, typhoid or other transmissible disease in the recent past.</li>
          </ul>
        </p>
        <h3>DO NOT donate blood, if you have any of the following conditions</h3>
        <p><ul>
          <li><p>You’ve had cold or fever in the past 1 week.</li>
          <li><p>You are under treatment with antibiotics or any other medication.</li>
          <li><p>You have cardiac problems, hypertension, epilepsy, diabetes (on insulin therapy), history of cancer, chronic kidney or liver disease, bleeding tendencies, veneral disease etc.</li>
          <li><p>You’ve underwent major surgery in the last 6 months.</li>
          <li><p>You have undergone vaccination in the last 24 hours.</li>
          <li><p>You have had a miscarriage in the last 6 months or have been pregnant / lactating in the last one year.</li>
        </ul>
