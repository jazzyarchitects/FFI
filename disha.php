          <h3>About</h3>
          
          <p>What should I choose as a viable career option?
            How do I prepare myself for it? What all subjects do I need to study? What’s the degree I need to acquire? Which is the best college I could enroll into for this? These questions are what a student faces while preparing for further studies and we people, at Disha, tackle and solve, for students in schools of Dhanbad, Jharkhand. These students have a very little knowledge regarding viable career options. We aim to bring down this lack of information by making them aware of the various career fields there are, which are suitable for them and how they can pursue them by providing them the necessary information like about nearby colleges, fee structure, etc. DISHA volunteers organize counseling sessions in schools and make the students aware of the aspects of careers. The volunteers are trained by the experienced volunteers. The volunteers are also benefitted along with the students as they get the opportunity to fulfill their social responsibilities towards the society, along with the enhancement of their skills.</p>
          </div><br><hr>
          <div id="section2" >
           <h3>Vision</h3>
           <p>This branch of FFI was started with a vision that DISHA will eradicate, the chimera of misinformation and ignorance, so that no student pays the cost because of such things. As in various cases, parents are not well-informed over the current career options and are often misguided. Also, there are cases where due to financial constraints, higher studies aren’t a priority. DISHA provides the students with a medium through which they can explore their options when they complete their 10+2.
           </p>
           <h3>Future Plans</h3>
           <p>Mass career counselling session to cover a large no. of audience at a single stroke, thereby increasing our reach.</p>
