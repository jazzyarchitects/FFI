            <h3>Working</h3> 
            <p>DISHA volunteers organize counseling sessions in schools and make the students aware of the above mentioned aspects of careers. The volunteers are trained by the experienced volunteers. The volunteers are also benefitted along with the students as they get the opportunity to fulfill their social responsibilities towards the society. In DISHA, volunteers are trained to counsel classes from 9 to 12 in various schools. The session starts with a friendly meeting with those students, which help them to create an aura of ease before the start of counselling sessions as we don’t wish that they hesitate to ask anything from us. In the counselling sessions, these students are being told about various career options and also every aspect which helps them either socially or financially. Before the counselling sessions, volunteers are trained by the experienced volunteers so that they can carry out the session smoothly. For this, mock sessions for presenting career options and group discussion sessions are held to make volunteers more comfortable with most of the career options provided. Our volunteers are so much experienced that they get the chance for internships with various other NGOs too.
            </p>
